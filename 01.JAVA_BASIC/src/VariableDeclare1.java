
public class VariableDeclare1 {

	public static void main(String[] args) {
		int a;//		a라는 변수를 선언
		a = 10;//		a라는 변수에 10의 리터럴 값을 넣음(변수 초기화)
        System.out.println(a);
//       a 변수값 출력
        a = 50;
//      a 변수값 변경
        System.out.println(a);
//      a 변수값 출력
        
        int b;
        int c;
        int d,e;
        
        b=12;
        c=34;
        d=67;
        e=90;
        
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        
	}

}
